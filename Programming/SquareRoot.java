public class SquareRoot {

    public static void main(String arg[]) {
        boolean flag = false;
        int n = 37;
        for (int i = 0; i < n; i++) {
            if ((i * i) == n) {
                flag = true;
            }

            if ((i * i) == n) {
                flag = true;
            }

        }

        if (flag) {
            System.out.println("its Perfect square");
        } else {
            System.out.println("its not Perfect square");

        }
    }
}
